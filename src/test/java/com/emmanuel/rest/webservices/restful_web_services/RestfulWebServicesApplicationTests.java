
/*
What is a web service?
A web service is a software module or component that provides functionality for other software applications or clients over a network.
It is a collection of open protocols and standards used for exchanging data between systems or applications.
Software applications written in various programming languages and running on various platforms can use web services to exchange data over computer networks like the Internet in a manner similar to inter-process communication on a single computer.

What is REST?
REST stands for Representational State Transfer. It is an architectural style for designing networked applications.
It relies on a stateless, client-server, cacheable communications protocol -- and in virtually all cases, the HTTP protocol is used.
REST is often used in mobile applications, social networking web applications, mashup tools, and automated business processes.

Web Service: A software system designed to support interoperable (platform independent), machine-to-machine interaction over a network.

Message Exchange Format: Making a web service platform independent involves using a general communication (request and response) format. E.g XML or JSON

Web Service Category
SOAP Web Services: Using SOAP-XML as a message exchange format.
REST Web Services: Uses HTTP methods (GET, POST, DELETE,...) for executing operations.
* */
